namespace App\Http\Requests;

use Illuminate\Foundation\Http\FormRequest;

class ProdukRequest extends FormRequest
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return true;
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            'pelapak'           => 'required|min:5|max:15',
            'kontak_pelapak'    => 'required|min:5|max:15',
            'produk'            => 'required|string|min:5|max:255',
            'deskripsi_produk'  => 'required|string|min:5|max:255',
            'kategori_produk'   => 'required|string|min:5|max:255',
            'harga'             => 'required',
            'stok'              => 'required|integer|min:4|max:100',
            'satuan'            => 'required|string|min:2|max:15',
            'potongan'          => 'required|max:15',
            'foto'              => 'file|mimes:jpg,jpeg,png,gif,pdf|max:2048',
        ];
    }
}
