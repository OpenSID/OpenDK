namespace App\Http\Controllers\Page;

use App\Http\Controllers\Controller;
use App\Models\Produk;

class ProdukController extends Controller
{
    public function index()
    {
        $page_title       = 'Produk';
        $page_description = 'Daftar Produk';
        $produk = Produk::orderby('created_at')->paginate(20);
        return view('pages.produk.index', compact('page_title', 'page_description', 'produk'));
    }

    public function show(Produk $produk)
    {
        $page_title       = 'Produk';
        $page_description = 'Detail Barang Jualan : ' . $produk->produk;

        return view('page.produk.show', compact('page_title', 'page_description', 'produk'));
    }
}
